function add(a,b){
    return a+b;
}

function subtract(a,b){
    return a-b;
}

function multiply(a,b){
    return a*b;
}

function divide(a,b){
    return a/b;
}

let result1 = add(10,5);
let result2 = subtract(10,5);
let result3 = multiply(10,5);
let result4 = divide(10,5);

console.log(result1);
console.log(result2);
console.log(result3);
